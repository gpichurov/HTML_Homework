<?php

// Задача 12: Да се състави програма, която извежда всички
// естествени трицифрени числа, които нямат еднакви цифри т.е.
// 100,101, 606 и т.н. не се извеждат.

for ($i = 102; $i < 988; $i++) {
	
	$n0 = $i % 10;
	$n1 = ($i / 10) % 10;
	$n2 = ($i / 100) % 10;
	
	if (($n0 != $n1) && ($n0 != $n2) && ($n1 != $n2)){
		echo "$i ";
	}
}