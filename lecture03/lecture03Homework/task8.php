<?php

// Задача8: По зададено число n, да се изведе на екрана таблица по
// следния начин:
// n: 1    n: 2    n:3
// 0       11      222
//         33      444
//                 666

require_once 'readline.php';

$n = readline('Въведете число: ');

for ($i = 0; $i < $n; $i++) {
	for ($j = 0; $j < $n; $j++) {
		echo $n - 1 + 2 * $i;
	}
	echo PHP_EOL;
}