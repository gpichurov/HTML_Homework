function bomb(&$array, $a, $b) {
	if ($array[$a][$b] === '#'){
		return;
	}
	if (($array[$a + 1][$b] !== '#' && $array[$a + 1][$b] !== 'X') && isset($array[$a + 1][$b])){
		$array[$a + 1][$b] = 'X';
		bomb($array, $a + 1, $b);
	}
